//38) Escreva um programa que mostre na tela a seguinte contagem:
//6 7 8 9 10 11 Acabou!

public class Exercicio38_Contagem {

	public static void main(String[] args) {

		for (int i = 6; i <= 11; i++) {
			System.out.println(i);
		}

		System.out.println("Acabou!");
	}

}
